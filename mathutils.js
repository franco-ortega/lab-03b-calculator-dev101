export function addNumbers(numberOne, numberTwo) {
    return numberOne + numberTwo;
}

export function subtractNumbers(numberOne, numberTwo) {
    return numberOne - numberTwo;
}

export function multiplyNumbers(numberOne, numberTwo) {
    return numberOne * numberTwo;
}

export function divideNumbers(numberOne, numberTwo) {
    return numberOne / numberTwo;
}

